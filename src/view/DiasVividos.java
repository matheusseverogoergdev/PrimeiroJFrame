    private void jbCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbCalcularActionPerformed
        // TODO add your handling code here:
        if (jtfIAno.getText().isBlank() || jtfIMes.getText().isBlank() || jtfIAno.getText().isBlank()) {
            JOptionPane.showMessageDialog(this, "Todos os campos devem ser preenchidos!");
            jtfIAno.requestFocus();
        } else {
            // Iniciando as variÃ¡veis
            String ano, mes, dia, dataString;
            LocalDate data, dataAtual = LocalDate.now();
            long qntDias;
            
            // Inserindo o valor dos campos nas variÃ¡veis
            if (Integer.parseInt(jtfIAno.getText()) > dataAtual.getYear()) {
                JOptionPane.showMessageDialog(this, "Erro, o ano de nascimento"
                        + " nÃ£o pode ser maior que o ano atual.");
                jtfIAno.requestFocus();
            } else {
                ano = jtfIAno.getText();
                if (Integer.parseInt(jtfIMes.getText()) > dataAtual.getMonthValue() && Integer.parseInt(jtfIAno.getText()) == dataAtual.getYear()) {
                    JOptionPane.showMessageDialog(this, "Erro, o mÃªs de nascimento"
                        + " nÃ£o pode ser maior que o ano atual.");
                    jtfIMes.requestFocus();
                } else {
                    mes = jtfIMes.getText();
                    if (Integer.parseInt(jtfIDia.getText()) > dataAtual.getDayOfMonth() && 
                            Integer.parseInt(jtfIMes.getText()) == dataAtual.getMonthValue() && 
                            Integer.parseInt(jtfIAno.getText()) == dataAtual.getYear()) {
                        
                        JOptionPane.showMessageDialog(this, "Erro, o dia de nascimento"
                        + " nÃ£o pode ser maior que o dia atual.");
                    } else {
                        dia = jtfIDia.getText();

                        DateTimeFormatter formatador = DateTimeFormatter.ofPattern("yyyy/MM/dd");
                        dataString = ano + "/" + mes + "/" + dia;
                        data = LocalDate.parse(dataString, formatador);

//                        qntDias = DAYS.between(data.atStartOfDay(), dataAtual.atStartOfDay());
                        qntDias = data.until(dataAtual, DAYS);
                        JOptionPane.showMessageDialog(this, "Desde o dia em que vocÃª nasceu atÃ© o dia de hoje se passaram: " + qntDias + " dias");
                        
                        limpar();
                    }
                }
            }
        }
    }//GEN-LAST:event_jbCalcularActionPerformed

